function onPageSubmitGPSHIS(action) {
	if (action=="a" || action=="sc") {
		if (isInputEmpty("u_cgcd_location")) return false;
		if (isInputEmpty("u_cgcd_activity")) return false;
		if (isInputEmpty("u_cgcd_note")) return false;
		if (isInputEmpty("u_mi_scpsp")) return false;
	}
	return true;
}

