function onPageLoadGPSHIS() {
	try {
		if (getVar("formSubmitAction")=="a") {
			if(window.opener.getVar("objectcode")=="U_HISOPS") {
				setInput("u_reftype","OP");
			} else {
				setInput("u_reftype","IP");
			}
			setInput("u_refno",window.opener.getInput("docno"),true);
		}
		
	} catch (theError) {
	}
	
}

